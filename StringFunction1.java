package module1programs;

public class StringFunction1 {

	public static void main(String[] args) {
		
		String a = "Automation";
		
		String a1 = a.substring(2);  // give output from 2nd index till end
		
		System.out.println(a1);
		
		String a2 = a.substring(2,8);  // give output between 2nd and 8th index (2 3 4 5 6 7)
		
		System.out.println(a2);

	}

}
